"""Обработчик справки"""
from aiogram.types import Message


async def help_handler(message: Message):
    """Обработка команды справки"""
    help_text = (
        "📖 Справка по использованию бота\n\n"
        "🔍 Добавление товара:\n"
        "1. Нажмите '📦 Добавить товар'\n"
        "2. Введите название товара\n"
        "3. Введите диапазон цен (формат: 500-65000)\n"
        "4. Введите слова исключения (чехол,case) или 'пропустить'\n\n"
        "✏ Редактирование:\n"
        "Выберите товар из списка и нажмите 'Редактировать'\n\n"
        "🗑 Удаление:\n"
        "Выберите товар и подтвердите удаление\n\n"
        "📊 Мониторинг:\n"
        "Бот автоматически отслеживает товары и отправляет уведомления при:\n"
        "• Появлении нового товара в диапазоне\n"
        "• Снижении цены\n"
        "• Попадании цены в ваш диапазон"
    )
    
    await message.answer(help_text)
